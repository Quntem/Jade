function generateToken() {
  return `${VPN_TOKEN_PREFIX}${randomBytes(32).toString("base64url")}`;
}

function ipToNumber(ip: string) {
  return ip.split(".").reduce((value, octet) => {
    const parsed = Number(octet);
    if (!Number.isInteger(parsed) || parsed < 0 || parsed > 255) {
      throw new Error(`Invalid IPv4 address: ${ip}`);
    }
    return (value << 8) + parsed;
  }, 0) >>> 0;
}

function numberToIp(value: number) {
  return [
    (value >>> 24) & 255,
    (value >>> 16) & 255,
    (value >>> 8) & 255,
    value & 255,
  ].join(".");
}

function normalizePort(value: number) {
  if (!Number.isInteger(value) || value < 1 || value > 65535) {
    throw new VpnError("endpointPort must be between 1 and 65535", 400);
  }

  return value;
}

function normalizeRequiredString(value: string, field: string) {
  const trimmed = value.trim();
  if (!trimmed) {
    throw new VpnError(`${field} is required`, 400);
  }
  return trimmed;
}

function normalizeOptionalString(value: unknown) {
  return typeof value === "string" && value.trim().length > 0
    ? value.trim()
    : null;
}

function slugify(value: string) {
  return value
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "")
    .slice(0, 40) || "vpn-client";
}

function normalizeVpnClientStatus(
  value: unknown,
  fallback: VpnClientStatus = "Ready",
): VpnClientStatus {
  if (
    value === "Pending" ||
    value === "Ready" ||
    value === "Delivered" ||
    value === "Degraded" ||
    value === "Disabled"
  ) {
    return value;
  }

  return fallback;
}

async function getDefaultHub() {
  const hub = await prismaClient.vpnHub.findFirst({
    where: {
      deletedAt: null,
    },
    orderBy: {
      createdAt: "asc",
    },
  });

  if (!hub) {
    throw new VpnError("VPN hub has not been created", 409);
  }

  return hub;
}

async function resolveVpnHub(hubId?: string) {
  if (!hubId) {
    return await getDefaultHub();
  }

  const hub = await prismaClient.vpnHub.findFirst({
    where: {
      id: hubId,
      deletedAt: null,
    },
  });

  if (!hub) {
    throw new VpnError("VPN hub not found", 404);
  }

  return hub;
}

