async function getClientAllowedIps({
  tx,
  hubId,
}: {
  tx: Prisma.TransactionClient;
  hubId: string;
}) {
  const peers = await tx.vpnPeer.findMany({
    where: {
      hubId,
      enabled: true,
      deletedAt: null,
    },
    orderBy: {
      tunnelIp: "asc",
    },
    select: {
      tunnelIp: true,
    },
  });
  const clients = await tx.vpnClient.findMany({
    where: {
      hubId,
      enabled: true,
      deletedAt: null,
    },
    select: {
      tunnelIp: true,
    },
  });
  const allowedIps = new Set<string>([
    `${HUB_TUNNEL_IP}/32`,
    ...peers.map((peer) => `${peer.tunnelIp}/32`),
    ...clients.map((client) => `${client.tunnelIp}/32`),
  ]);

  return [...allowedIps];
}

async function getHubAllowedClientIps({
  hubId,
  excludeTunnelIp,
}: {
  hubId: string;
  excludeTunnelIp?: string;
}) {
  const [peers, clients] = await Promise.all([
    prismaClient.vpnPeer.findMany({
      where: {
        hubId,
        enabled: true,
        deletedAt: null,
      },
      select: {
        tunnelIp: true,
      },
    }),
    prismaClient.vpnClient.findMany({
      where: {
        hubId,
        enabled: true,
        deletedAt: null,
      },
      select: {
        tunnelIp: true,
      },
    }),
  ]);

  return [
    ...new Set([
      `${HUB_TUNNEL_IP}/32`,
      ...peers.map((peer) => `${peer.tunnelIp}/32`),
      ...clients
        .filter((client) => client.tunnelIp !== excludeTunnelIp)
        .map((client) => `${client.tunnelIp}/32`),
    ]),
  ];
}

