function renderVpnClientConfig({
  client,
  hub,
  privateKey,
  allowedIps,
}: {
  client: { name: string; tunnelIp: string };
  hub: { endpointHost: string; endpointPort: number; publicKey: string };
  privateKey: string;
  allowedIps: string[];
}): string {
  return [
    "# Generated by Jade VPN Client setup.",
    `# Client: ${client.name}`,
    "[Interface]",
    `PrivateKey = ${privateKey}`,
    `Address = ${client.tunnelIp}/32`,
    "",
    "[Peer]",
    `PublicKey = ${hub.publicKey}`,
    `Endpoint = ${hub.endpointHost}:${hub.endpointPort}`,
    `AllowedIPs = ${allowedIps.join(", ")}`,
    `PersistentKeepalive = ${DEFAULT_PERSISTENT_KEEPALIVE}`,
    "",
  ].join("\n");
}

